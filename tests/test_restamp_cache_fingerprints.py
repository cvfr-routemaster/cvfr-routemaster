"""Tests for the cache-fingerprint restamping helper.

The helper (``scripts/_restamp_cache_fingerprints.py``) exists to fix
a build-pipeline bug specific to Linux releases built in WSL: the
dev cache JSONs in ``.cvfr_routemaster/`` store PDF mtimes with NTFS
sub-second precision (because they were generated by the app running
natively on Windows), but ``shutil.copy2`` reading those same PDFs
through WSL's ``/mnt/c/`` 9P bridge **floors mtimes to whole seconds**.
The shipped cache JSON's stored mtime and the shipped PDF's live
mtime end up disagreeing at the nanosecond level, every cache
fingerprint check rejects on first launch, and the user is hit with:

  * a re-OCR pass (needs Tesseract installed — not always present),
  * an altitude-arrow re-extraction (~30 s),
  * a map-PDF re-render to PNG (~30 s),
  * a modal "please re-calibrate the chart" dialog asking the user
    to click 8 anchor waypoints manually.

That last one is the showstopper that triggered the bug investigation:
on WSLg's Wayland compositor the dialog rendered offscreen and the
app appeared to hang at startup with only a tiny ``[WARN:COPY...]``
window slice in the taskbar.

The restamp helper rewrites every ``mtime_ns`` field in every cache
JSON to match the live PDF mtimes after ``_copy_charts`` and
``_copy_seed_cache``, so the fingerprints match by construction.

These tests cover:

  1. The happy path: a synthetic release tree with a cache JSON
     whose stored mtime is wrong gets it corrected, and the file is
     rewritten with the new mtime + preserved structure.
  2. Idempotency: running the helper twice in a row is a no-op the
     second time.
  3. Each of the five real cache file schemas — single-PDF
     (``waypoints_cache.json``, ``altitude_arrows_*.json``),
     dual-PDF flat (``map_images_meta.json``), and dual-PDF nested
     (``geo_calibration.json``) — all get their mtimes updated
     correctly.
  4. Edge cases: missing cache file (skipped, not failed), missing
     PDF the cache references (reported as ``missing_pdfs``, not
     crashed), absent ``mtime_ns`` field (skipped, not crashed),
     malformed top-level JSON (skipped).
  5. The CLI entrypoint (``python3 -m scripts._restamp_cache_fingerprints
     <dir>``): prints a summary, exits 0 on clean updates, exits 1
     when a referenced PDF is missing.
  6. Build-script integration: both ``scripts/build_release.py``
     and ``scripts/build_release_for_linux.py`` call the helper
     after ``_copy_seed_cache``.
  7. The regression that motivated this whole helper: the verbatim
     cache JSONs the broken WSL build shipped (sub-second mtimes
     in ``geo_calibration.json``) get fixed to match
     whole-second PDF mtimes when run through the helper.
"""

from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path

import pytest


# Match the production cache layout constants. Duplicated here rather
# than imported so a future move/rename of the production constants
# trips a clear test failure ("name not exported") rather than the
# tests silently picking up the new value.
RELEASE_PDF_SUBDIR = "map-pdfs"
RELEASE_CACHE_SUBDIR = ".cvfr_routemaster"


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _make_release_tree(
    root: Path,
    pdfs: dict[str, int],
) -> Path:
    """Build a synthetic release tree at ``root``.

    Args:
        root: tmp directory that will become the release root.
        pdfs: mapping of PDF filename → mtime_ns to stamp on the
            created file. We use ``os.utime`` rather than relying on
            the filesystem's natural creation mtime so tests are
            deterministic across runs and machines.

    Returns:
        ``root`` (for convenience).
    """
    pdf_dir = root / RELEASE_PDF_SUBDIR
    cache_dir = root / RELEASE_CACHE_SUBDIR
    pdf_dir.mkdir(parents=True)
    cache_dir.mkdir(parents=True)
    for name, mtime_ns in pdfs.items():
        pdf_path = pdf_dir / name
        # Content size is fingerprinted too; we don't test mismatched
        # sizes (separate concern, not what this helper touches) but
        # we want a non-zero size so the fixtures look realistic.
        pdf_path.write_bytes(b"%PDF-1.4\nfake content for tests\n")
        import os

        os.utime(pdf_path, ns=(mtime_ns, mtime_ns))
    return root


def _write_cache(root: Path, name: str, data: dict) -> Path:
    """Write a JSON cache file under ``root/.cvfr_routemaster/``."""
    path = root / RELEASE_CACHE_SUBDIR / name
    path.write_text(json.dumps(data, indent=2) + "\n", encoding="utf-8")
    return path


def _read_cache(root: Path, name: str) -> dict:
    return json.loads((root / RELEASE_CACHE_SUBDIR / name).read_text(encoding="utf-8"))


