def test_restamp_corrects_stale_waypoints_mtime(tmp_path: Path) -> None:
    """The canonical drift case: cache JSON has sub-second mtime
    precision (Windows-native), PDF has whole-second mtime (WSL
    9P-floored). Helper rewrites the cache to match the PDF.
    """
    from scripts._restamp_cache_fingerprints import (
        DEFAULT_BACK_PDF,
        restamp_cache_fingerprints,
    )

    live_pdf_mtime = 1777797103_000_000_000
    stale_cache_mtime = 1777797103_955_812_000

    root = _make_release_tree(tmp_path, {DEFAULT_BACK_PDF: live_pdf_mtime})
    _write_cache(
        root,
        "waypoints_cache.json",
        {
            "back_pdf": {
                "path": f"/release-linux/map-pdfs/{DEFAULT_BACK_PDF}",
                "mtime_ns": stale_cache_mtime,
                "size": 202103,
            },
            "records": [],
        },
    )

    report = restamp_cache_fingerprints(root)

    assert report.updates == {
        "waypoints_cache.json": [
            ("back_pdf", stale_cache_mtime, live_pdf_mtime)
        ]
    }
    # Other caches not present in this fixture ⇒ legitimately skipped.
    # The check that matters is "nothing crashed and nothing was
    # flagged as a build error".
    assert report.missing_pdfs == []

    cached = _read_cache(root, "waypoints_cache.json")
    assert cached["back_pdf"]["mtime_ns"] == live_pdf_mtime
    # The size field is untouched on purpose — restamping mtime only.
    assert cached["back_pdf"]["size"] == 202103
    # Unrelated keys preserved.
    assert cached["records"] == []


def test_restamp_is_idempotent(tmp_path: Path) -> None:
    """Second call after a successful first call reports zero updates."""
    from scripts._restamp_cache_fingerprints import (
        DEFAULT_BACK_PDF,
        restamp_cache_fingerprints,
    )

    live_pdf_mtime = 1777797103_000_000_000
    root = _make_release_tree(tmp_path, {DEFAULT_BACK_PDF: live_pdf_mtime})
    _write_cache(
        root,
        "waypoints_cache.json",
        {
            "back_pdf": {
                "path": "ignored",
                "mtime_ns": 999,
                "size": 1,
            },
        },
    )

    first = restamp_cache_fingerprints(root)
    assert first.total_fields_updated() == 1

    second = restamp_cache_fingerprints(root)
    assert second.total_fields_updated() == 0
    assert second.updates == {}


def test_restamp_geo_calibration_dual_nested_pdf(tmp_path: Path) -> None:
    """``geo_calibration.json`` has two PDFs nested under
    ``north.pdf`` and ``south.pdf``. This is the schema that
    motivated the bug — verbatim from the broken WSL build.
    """
    from scripts._restamp_cache_fingerprints import (
        DEFAULT_NORTH_PDF,
        DEFAULT_SOUTH_PDF,
        restamp_cache_fingerprints,
    )

    north_live = 1777797121_000_000_000
    south_live = 1777797137_000_000_000
    north_stale = 1777797121_332_421_700
    south_stale = 1777797137_316_700_700

    root = _make_release_tree(
        tmp_path,
        {DEFAULT_NORTH_PDF: north_live, DEFAULT_SOUTH_PDF: south_live},
    )
    _write_cache(
        root,
        "geo_calibration.json",
        {
            "version": 1,
            "north": {
                "pdf": {
                    "path": f"<repo-root>\\\\{DEFAULT_NORTH_PDF}",
                    "mtime_ns": north_stale,
                    "size": 7910912,
                },
                "points": [{"code": "HOTRM"}],
                "map_layout": {"x": 0.0, "y": 0.0, "scale": 1.0},
            },
            "south": {
                "pdf": {
                    "path": f"<repo-root>\\\\{DEFAULT_SOUTH_PDF}",
                    "mtime_ns": south_stale,
                    "size": 8697353,
                },
                "points": [{"code": "OMER"}],
                "map_layout": {"x": 100.0, "y": 200.0, "scale": 1.0},
            },
        },
    )

    report = restamp_cache_fingerprints(root)

    updates = report.updates["geo_calibration.json"]
    assert sorted(updates) == sorted(
        [
            ("north.pdf", north_stale, north_live),
            ("south.pdf", south_stale, south_live),
        ]
    )

    cached = _read_cache(root, "geo_calibration.json")
    assert cached["north"]["pdf"]["mtime_ns"] == north_live
    assert cached["south"]["pdf"]["mtime_ns"] == south_live
    # Anchor data and map layout (the actually-valuable calibration
    # state) must be preserved bit-for-bit through the restamp.
    assert cached["north"]["points"] == [{"code": "HOTRM"}]
    assert cached["south"]["points"] == [{"code": "OMER"}]
    assert cached["south"]["map_layout"] == {"x": 100.0, "y": 200.0, "scale": 1.0}


