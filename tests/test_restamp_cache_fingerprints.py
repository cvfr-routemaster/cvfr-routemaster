def test_load_map_layout_qsettings_wins_over_shipped_file(
    tmp_path: Path, _isolated_qsettings: Path
) -> None:
    """User customisation precedence: once the user moves a sheet
    on the release machine, their layout is saved to QSettings via
    :func:`save_map_layout`. From that point on, QSettings is the
    source of truth — the shipped-file fallback must NOT clobber
    the user's choice.

    Without this priority the user would have to dismiss the
    shipped layout every launch ("why did the sheets move back?").
    """
    from cvfr_routemaster.settings_store import (
        load_map_layout,
        save_map_layout,
    )
    from scripts._restamp_cache_fingerprints import write_shipped_map_layout

    root = _make_release_tree(tmp_path, {})
    _write_cache(
        root,
        "geo_calibration.json",
        {
            "north": {"map_layout": {"x": 0.0, "y": 0.0, "scale": 1.0}},
            "south": {"map_layout": {"x": 999.0, "y": 999.0, "scale": 1.0}},
        },
    )
    write_shipped_map_layout(root)

    # User makes their own arrangement.
    save_map_layout(
        north_x=11.0,
        north_y=22.0,
        north_scale=1.0,
        south_x=33.0,
        south_y=44.0,
        south_scale=1.0,
        selected="north",
    )

    loaded = load_map_layout(root)
    assert loaded is not None
    # User's saved values, NOT the shipped file's 999s.
    assert loaded["north_x"] == 11.0
    assert loaded["south_x"] == 33.0
    assert loaded["selected"] == "north"


