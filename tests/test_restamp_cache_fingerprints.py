@pytest.mark.parametrize(
    "build_script,subdir_name",
    [
        ("scripts/build_release.py", "release"),
        ("scripts/build_release_for_linux.py", "release-linux"),
    ],
)
def test_build_script_calls_derived_files_after_seed_cache(
    build_script: str,
    subdir_name: str,  # noqa: ARG001 — kept for parametrize symmetry
) -> None:
    """Both build scripts must invoke ``_write_shipped_derived_files``
    AFTER ``_copy_seed_cache``.

    v3.3+ replaced the old ``_restamp_cache_fingerprints`` build
    step (which depended on shipped PDFs being in place — gone
    now that charts are downloaded at runtime) with
    ``_write_shipped_derived_files``, which bakes:

    * ``chart_sources.json`` (the three default CAAI URLs),
    * ``map_layout.json`` (from the calibration), and
    * ``font_settings.json`` (from the dev's QSettings).

    ``write_shipped_map_layout`` and the path-sanitiser both read
    the shipped cache JSONs, so ``_copy_seed_cache`` must run
    first. We pin the call ordering by parsing the build script
    source and confirming the function names appear in the
    expected sequence.
    """
    repo_root = Path(__file__).resolve().parents[1]
    src = (repo_root / build_script).read_text(encoding="utf-8")

    def _find_call(needle: str) -> int:
        marker = f"    {needle}()"
        pos = src.find(marker)
        assert pos != -1, f"{build_script} does not call {needle}() in main()"
        return pos

    seed_pos = _find_call("_copy_seed_cache")
    derived_pos = _find_call("_write_shipped_derived_files")
    sanitize_pos = _find_call("_sanitize_shipped_cache_paths")

    assert seed_pos < derived_pos, (
        f"{build_script}: _write_shipped_derived_files must run AFTER "
        "_copy_seed_cache (it reads the shipped cache JSONs)"
    )
    assert derived_pos < sanitize_pos, (
        f"{build_script}: _sanitize_shipped_cache_paths must run AFTER "
        "_write_shipped_derived_files (the sanitiser is the last write "
        "pass over the shipped JSONs; running it earlier would let dev "
        "paths leak through the later derived-file writes)"
    )

    # Belt-and-braces: the removed v3.2 build steps must NOT
    # reappear via a copy-paste mistake. ``_copy_charts()`` and
    # ``_restamp_cache_fingerprints()`` belong to the pre-v3.3
    # era when PDFs were shipped — they have no place in the
    # new pipeline and silently re-adding them would cause
    # subtle regressions (an empty release/map-pdfs/ folder
    # appearing, or a restamp pass that fails on the now-absent
    # PDFs).
    for forbidden in ("_copy_charts()", "_restamp_cache_fingerprints()"):
        # Match only as a bare call inside main() (4-space indent),
        # not as a docstring mention.
        forbidden_marker = f"    {forbidden}"
        assert forbidden_marker not in src, (
            f"{build_script}: v3.3+ must NOT call {forbidden} — the "
            f"chart-fetch refactor removed that step. Found a bare "
            f"call at indent-4 in main()."
        )


# ---------------------------------------------------------------------------
# Shipped map_layout.json generation
# ---------------------------------------------------------------------------
#
# These tests cover the fix for the second-order startup bug found
# while debugging release v2: ``geo_calibration.json`` records the
# on-screen sheet position the dev used when capturing the
# calibration, but the **actual** current sheet position on first
# launch is loaded from QSettings (per-user, doesn't ship with the
# release). On a fresh QSettings, the sheets land at the hard-coded
# vertical-stack default, ``map_layout_matches`` rejects the
# calibration as "different layout", and the modal "please
# re-calibrate" dialog fires.
#
# Earlier iterations of this code tried to *normalise the
# calibration's map_layout to the release default* — destroying the
# dev's intent. The right fix, captured by these tests, is the
# opposite: ship a release-side ``map_layout.json`` that
# ``load_map_layout`` consults as a second-priority fallback after
# QSettings, so first launch on any machine restores **the dev's**
# calibrated layout. The user's own arrangements on the release
# machine still win once they make them (saved to QSettings).


